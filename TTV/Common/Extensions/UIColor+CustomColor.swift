import Foundation
import UIKit

extension UIColor {
    static var bg_1_1b1b23: UIColor? {
        return UIColor.init(hex: "#1B1E23")
    }
    
    static var aaaaaa: UIColor? {
        return UIColor.init(hex: "#AAAAAA")
    }
    
    static var textFieldBackground: UIColor? {
        return UIColor.init(hex: "#24282F")
    }
    
    static var textFieldFocus: UIColor? {
        return UIColor.init(hex: "#7450FF")
    }
    
    static var loginWarningColor: UIColor? {
        return UIColor.init(hex: "#FF5F0A")
    }
}
